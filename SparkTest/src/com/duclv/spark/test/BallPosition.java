package com.duclv.spark.test;

public class BallPosition {
	private long ts;
	private int x = 0;
	private int y = 0;
	private int z = 0;
	private long v = 0;
	private long a = 0;
	
	public long getTs() {
		return ts;
	}
	public void setTs(long ts) {
		this.ts = ts;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getZ() {
		return z;
	}
	public void setZ(int z) {
		this.z = z;
	}
	public long getV() {
		return v;
	}
	public void setV(long v) {
		this.v = v;
	}
	public long getA() {
		return a;
	}
	public void setA(long a) {
		this.a = a;
	}
	
}
